
//post incrementation a++
let a=3;
let b;
b=a++;          //value is assigned first to b then the value of a is incremented
console.log(b);
console.log(a);

// pre incrementation ++a
let c=3;
let d;
d=++c;       // value of c is incremented first then the value is asigned to d
console.log(d);
console.log(c);